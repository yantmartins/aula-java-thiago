public class Vetor2 {

    public static void main(String[] args) {
        
        int num = 10; //tamanho do vetor
        int[] vet = new int[num]; //declaracao e alocacao de espaco para o vetor "v"
        int i; // indice ou posicao

        // processando os "n" elementos do vetor "v"   
        for(i=0; i<num; i++) {
            vet[i] = i; // na posicao "i" do vetor "vet" // armazena o vaor da variavel "i"
        }
   
    }
}